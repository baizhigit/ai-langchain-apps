# Practical planning and logistics
# - Getting there and around
#   - Almaty International Airport (ALA) connects directly to major hubs including Istanbul, Dubai, Frankfurt, Seoul, and Moscow. Domestic flights, overnight trains, buses, and shared taxis provide additional entry options (Postcards from the World).
#   - Getting around the city emphasizes taxis, ride-hailing apps such as Yandex Go, and the metro. Yandex is specifically highlighted for airport pickups and city travel (Postcards from the World).

# - Currency, payments, and language
#   - The local currency is the Kazakh tenge (KZT). A frequently cited conversion is around 1000 tenge ≈ 2 USD (as of June 2024) in the source material (Postcards from the World). Cards are widely accepted in most urban venues, but cash remains useful for markets and more remote or internet-poor locations (Postcards from the World).
#   - Official languages are Kazakh and Russian; English is less commonly spoken, so travelers are advised to use translation apps and consider buying a SIM/eSIM in advance (Postcards from the World).

# - Food and dining
#   - Quick dining picks include Sandyq (local cuisine with live music), Tyubeteika, and Abay Restaurant. Typical dishes to try include beshbarmak, kumis, plov, baursaki, and shashlik. These options reflect the city’s traditional cuisine as described in the source guides (Postcards from the World).

# - Practical travel tips from the guides
#   - The material emphasizes a mix of city-based exploration and day trips to the mountains, highlighting the ease of combining urban sights with nature experiences in a single trip (Advantour; Secret Flying; Postcards from the World).
#   - Several guides point to the accessibility of hill views via cable cars (Kok Tobe Hill) as a practical and scenic option for visitors who want to minimize hiking while still capturing expansive views (Secret Flying; Backpack Adventures).
#   - Some guides note additional natural destinations outside the city, such as Charyn Canyon and the Singing Dune in Altyn-Emel National Park, which are feasible as longer side trips from Almaty (Advantour).

